// 1. Arrow Function Without Argument and With Return Type

const greet = () => "Hello, World!";
console.log(greet());


// 2. Arrow Function Without Argument and Without Return Type

const sayHello = () => console.log("Hello!");
sayHello(); 


// 3. Arrow Function With Argument and With Return Type

const add = (a, b) => a + b;
console.log(add(5, 3)); 


// 4. Arrow Function With Argument and Without Return Type

const displaySum = (a, b) => console.log(`The sum is ${a + b}`);
displaySum(5, 3); 